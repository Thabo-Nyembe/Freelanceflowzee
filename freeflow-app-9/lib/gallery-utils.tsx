/**
 * Validate image file
 */
export const validateImageFile = (file: File): { valid: boolean; error?: string } => {
  const maxSize = 50 * 1024 * 1024 // 50 MB
  const allowedFormats = ['image/jpeg', 'image/jpg', 'image/png', 'image/gif', 'image/webp', 'image/svg+xml', 'video/mp4', 'video/mov']

  if (file.size > maxSize) {
    return { valid: false, error: `File size exceeds 50 MB limit (${formatFileSize(file.size)})` }
  }

  if (!allowedFormats.includes(file.type)) {
    return { valid: false, error: `Unsupported file format: ${file.type}` }
  }

  return { valid: true }
}

