// ================================
// TYPESCRIPT INTERFACES
// ================================

export interface GalleryImage {
  id: string
  title: string
  description: string
  fileName: string
  fileSize: number // bytes
  width: number
  height: number
  format: string
  url: string
  thumbnail: string
  uploadDate: string
  modifiedDate?: string
  tags: string[]
  albumId: string | null
  isFavorite: boolean
  type: 'image' | 'video' | 'audio' | 'document'
  category: string
  client?: string
  project?: string
  likes: number
  comments: number
  views: number
  downloads: number
  shares: number
  metadata?: ImageMetadata
  exifData?: ExifData
  colorPalette?: string[]
  aiGenerated?: boolean
  sourcePrompt?: string
  processingStatus?: 'pending' | 'processing' | 'completed' | 'failed'
  userId?: string
  isPublic?: boolean
  shareUrl?: string
}

export interface Album {
  id: string
  name: string
  description: string
  coverImage: string
  imageCount: number
  createdDate: string
  modifiedDate?: string
  totalSize: number
  isPublic: boolean
  shareUrl?: string
  password?: string
  userId?: string
  tags?: string[]
  views?: number
  downloads?: number
}

export interface ImageCollection {
  id: string
  name: string
  description: string
  imageIds: string[]
  createdBy: string
  createdDate: string
  isAutoGenerated?: boolean
  rules?: CollectionRule[]
}

export interface CollectionRule {
  field: string
  operator: 'equals' | 'contains' | 'greater_than' | 'less_than'
  value: string | number
}

export interface ImageMetadata {
  camera?: string
  lens?: string
  iso?: number
  aperture?: string
  shutterSpeed?: string
  focalLength?: string
  location?: string
  coordinates?: { lat: number; lng: number }
  software?: string
  artist?: string
  copyright?: string
  keywords?: string[]
}

export interface ExifData {
  make: string
  model: string
  dateTime: string
  orientation: number
  software: string
  flash?: string
  whiteBalance?: string
  meteringMode?: string
  exposureMode?: string
}

export interface ImageTag {
  id: string
  name: string
  color?: string
  category?: string
  usageCount: number
  createdDate: string
}

export interface ImageComment {
  id: string
  imageId: string
  userId: string
  userName: string
  userAvatar?: string
  text: string
  createdDate: string
  likes: number
  replies?: ImageComment[]
}

export interface ImageShare {
  id: string
  imageId: string
  shareUrl: string
  password?: string
  expiresAt?: string
  permissions: SharePermission[]
  views: number
  downloads: number
  createdDate: string
}

export type SharePermission = 'view' | 'download' | 'comment' | 'edit'

export interface ImageEdit {
  id: string
  imageId: string
  userId: string
  editType: 'crop' | 'resize' | 'filter' | 'adjustment' | 'text' | 'other'
  parameters: Record<string, any>
  createdDate: string
  thumbnail?: string
}

export interface GalleryAnalytics {
  totalImages: number
  totalVideos: number
  totalAlbums: number
  totalStorage: number
  totalViews: number
  totalDownloads: number
  totalLikes: number
  totalComments: number
  topImages: GalleryImage[]
  topAlbums: Album[]
  recentUploads: GalleryImage[]
  storageByCategory: Record<string, number>
  viewsByDay: Array<{ date: string; views: number }>
}

export interface ImageFilter {
  categories?: string[]
  types?: Array<'image' | 'video' | 'audio' | 'document'>
  tags?: string[]
  albumIds?: string[]
  isFavorite?: boolean
  isPublic?: boolean
  aiGenerated?: boolean
  dateRange?: { start: string; end: string }
  minSize?: number
  maxSize?: number
  minDimensions?: { width: number; height: number }
  maxDimensions?: { width: number; height: number }
}

export type SortField = 'date' | 'name' | 'size' | 'type' | 'views' | 'likes' | 'downloads'
export type SortOrder = 'asc' | 'desc'

