/**
 * Filter images
 */
export const filterImages = (images: GalleryImage[], filter: ImageFilter): GalleryImage[] => {
  return images.filter(img => {
    // Category filter
    if (filter.categories && filter.categories.length > 0) {
      if (!filter.categories.includes(img.category)) return false
    }

    // Type filter
    if (filter.types && filter.types.length > 0) {
      if (!filter.types.includes(img.type)) return false
    }

    // Tags filter
    if (filter.tags && filter.tags.length > 0) {
      if (!filter.tags.some(tag => img.tags.includes(tag))) return false
    }

    // Album filter
    if (filter.albumIds && filter.albumIds.length > 0) {
      if (!img.albumId || !filter.albumIds.includes(img.albumId)) return false
    }

    // Favorite filter
    if (filter.isFavorite !== undefined) {
      if (img.isFavorite !== filter.isFavorite) return false
    }

    // Public filter
    if (filter.isPublic !== undefined) {
      if (img.isPublic !== filter.isPublic) return false
    }

    // AI generated filter
    if (filter.aiGenerated !== undefined) {
      if (img.aiGenerated !== filter.aiGenerated) return false
    }

    // Date range filter
    if (filter.dateRange) {
      const uploadDate = new Date(img.uploadDate)
      const startDate = new Date(filter.dateRange.start)
      const endDate = new Date(filter.dateRange.end)
      if (uploadDate < startDate || uploadDate > endDate) return false
    }

    // Size filters
    if (filter.minSize && img.fileSize < filter.minSize) return false
    if (filter.maxSize && img.fileSize > filter.maxSize) return false

    // Dimension filters
    if (filter.minDimensions) {
      if (img.width < filter.minDimensions.width || img.height < filter.minDimensions.height) return false
    }
    if (filter.maxDimensions) {
      if (img.width > filter.maxDimensions.width || img.height > filter.maxDimensions.height) return false
    }

    return true
  })
}

/**
 * Sort images
 */
export const sortImages = (images: GalleryImage[], field: SortField, order: SortOrder): GalleryImage[] => {
  const sorted = [...images].sort((a, b) => {
    let comparison = 0

    switch (field) {
      case 'date':
        comparison = new Date(b.uploadDate).getTime() - new Date(a.uploadDate).getTime()
        break
      case 'name':
        comparison = a.title.localeCompare(b.title)
        break
      case 'size':
        comparison = b.fileSize - a.fileSize
        break
      case 'type':
        comparison = a.type.localeCompare(b.type)
        break
      case 'views':
        comparison = b.views - a.views
        break
      case 'likes':
        comparison = b.likes - a.likes
        break
      case 'downloads':
        comparison = b.downloads - a.downloads
        break
    }

    return order === 'asc' ? -comparison : comparison
  })

  return sorted
}

